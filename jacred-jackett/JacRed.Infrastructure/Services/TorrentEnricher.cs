        var voices = new HashSet<string>();
        var languages = new HashSet<string>();
        var titlelower = torrent.Title?.ToLower() ?? string.Empty;

        // Специфичные для трекеров голосовые студии
        if (torrent.TrackerName == "lostfilm")
        {
            voices.Add("LostFilm");
            languages.Add("rus");
        }
        else if (torrent.TrackerName == "hdrezka")
        {
            voices.Add("HDRezka");
        }

        // Определение дубляжа
        if (Regex.IsMatch(titlelower, "( |x)(d|dub|дб|дуб|дубляж)(,| )")) voices.Add("Дубляж");

        // Поиск голосовых студий из allVoices
        foreach (var key in VoiceMap.Keys.Where(key => titlelower.Contains(key))) voices.Add(VoiceMap[key]);

        // Определение украинского языка
        if (titlelower.Contains("ukr")
            || titlelower.Contains("українськ")
            || titlelower.Contains("украинск")
            || torrent.TrackerName == "toloka")
            languages.Add("ukr");

        // Проверка украинских голосовых студий
        if (!languages.Contains("ukr"))
            foreach (var v in ukrVoices)
                if (voices.Contains(v))
                {
                    languages.Add("ukr");

                    break;
                }

        return (voices, languages);
    }

    private HashSet<int> ExtractSeasons(string title, string[]? types)
    {
        var seasons = new HashSet<int>();

        if (string.IsNullOrWhiteSpace(title) || types == null) return seasons;

        // Только для сериалов и аниме
        if (!types.Contains("serial") && !types.Contains("anime")) return seasons;

        // Ищем паттерны типа "S01", "Сезон 1", "Season 1"
        var seasonMatches = Regex.Matches(title, @"(?:S|С|Season|Сезон)\s*(\d+)", RegexOptions.IgnoreCase);

        foreach (Match match in seasonMatches)
            if (int.TryParse(match.Groups[1].Value, out var season))
                seasons.Add(season);

        return seasons;
    }
