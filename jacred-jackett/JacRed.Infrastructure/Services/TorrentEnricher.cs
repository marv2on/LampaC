    private void EnrichDetails(TorrentDetails torrent)
    {
        // Парсинг размера
        torrent.Size = ParseSize(torrent.SizeName);

        // Определение качества
        torrent.Quality = DetectQuality(torrent.Title);

        // Определение типа видео
        torrent.VideoType = DetectVideoType(torrent.Title);

        // Извлечение голосовых дорожек и языков
        var voices = DetectVoices(torrent);
        torrent.Voices = voices.Voices;
        torrent.Languages = voices.Languages;

        // Извлечение сезонов
        torrent.Seasons = ExtractSeasons(torrent.Title, torrent.Types);
    }

    private long ParseSize(string sizeName)
    {
        if (string.IsNullOrWhiteSpace(sizeName)) return 0;

        try
        {
            var size = 0.1;

            var gsize = Regex.Match(sizeName, "([0-9\\.,]+) (Mb|МБ|GB|ГБ|TB|ТБ)", RegexOptions.IgnoreCase)
                .Groups;

            if (!string.IsNullOrWhiteSpace(gsize[2].Value))
                if (double.TryParse(gsize[1]
                            .Value.Replace(",", "."), NumberStyles.Any, CultureInfo.InvariantCulture,
                        out size)
                    && size != 0)
                {
                    if (gsize[2]
                            .Value.ToLower() is "gb" or "гб")
                        size *= 1024;

                    if (gsize[2]
                            .Value.ToLower() is "tb" or "тб")
                        size *= 1048576;

                    return (long)(size * 1048576);
                }
        }
        catch
        {
        }

        return 0;
    }

    private int DetectQuality(string title)
    {
        if (string.IsNullOrWhiteSpace(title)) return 480;

        if (title.Contains("720p")) return 720;

        if (title.Contains("1080p")) return 1080;

        if (Regex.IsMatch(title.ToLower(), "(4k|uhd)( |\\]|,|$)") || title.Contains("2160p"))
            // Дополнительная проверка для 4K: вышел после 2000г, размер > 10GB, есть пометка о 4K
            return 2160;

        return 480;
    }

    private string DetectVideoType(string title)
    {
        if (string.IsNullOrWhiteSpace(title)) return "sdr";

        var titlelower = title.ToLower();

        if (Regex.IsMatch(titlelower, "(\\[| )hdr?(10\\+?)( |\\]|,|$)") ||
            (Regex.IsMatch(titlelower, "(10-bit|10 bit|10-бит|10 бит)") &&
             !Regex.IsMatch(titlelower, "(\\[| )sdr( |\\]|,|$)")))
            return "hdr";

        return "sdr";
    }

